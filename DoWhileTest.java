// do .. while test
public class DoWhileTest
{
   public static void main(String args[])
   {
      int counter = 1;  // initialise counter
      
      do
      {
         System.out.printf("%d ", counter);
         ++counter;
      } while (counter <= 10); // end do...while
      
      System.out.println(); // newline
      
   } //end main
} // end class DoWhileTest